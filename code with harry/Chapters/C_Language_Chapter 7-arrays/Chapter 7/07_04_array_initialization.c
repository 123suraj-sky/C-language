#include <stdio.h>

int main()
{
    int arr[] = {23, 58, 589};
    printf("The value of arr[0] is %d\n", arr[0]);
    printf("The value of arr[1] is %d\n", arr[1]);
    printf("The value of arr[2] is %d\n", arr[2]);
    

    float arr1[] = {2.3, 5.8, 5.89};
    printf("The value of arr[0] is %f\n", arr1[0]);
    printf("The value of arr[1] is %f\n", arr1[1]);
    printf("The value of arr[2] is %f\n", arr1[2]);
    
    return 0;
}